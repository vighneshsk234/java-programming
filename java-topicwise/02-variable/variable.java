public class variable {
    public static void main(String[] args) {
        int a = 10; // integer variable
        double b = 3.14; // double variable
        char c = 'A'; // character variable
        String d = "Hello"; // string variable

        System.out.println("Integer variable a: " + a);
        System.out.println("Double variable b: " + b);
        System.out.println("Character variable c: " + c);
        System.out.println("String variable d: " + d);
    }    
}
