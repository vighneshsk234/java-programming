public class string {
    public static void main(String[] args) {
        String name = "Vighnesh SK";
        String day = "Monday";
        String car = "Toyota";
        String email = "vighneshsk234@gmail.com";


        System.out.println("Hello " + name);
        System.out.println("Today is " + day);
        System.out.println("I drive a " + car);
        System.out.println("My email is " + email);
        System.out.println();

        // Array of strings and objects are reference type of variables
        // The above variables are reference type of variables because they store the reference to the actual string object in memory. When we assign a string literal to a variable, it creates a string object in memory and the variable holds a reference to that object. This allows us to manipulate and use the string data through the variable, while the actual string data is stored separately in memory.
        // We will learn that in further next topics.
            
    }
}